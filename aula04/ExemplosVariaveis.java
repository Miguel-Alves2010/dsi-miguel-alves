package aula04;

public class ExemplosVariaveis {
    public static void main(String[] args) {
        int n1, n2, n3;
        float n4;
        String l1;

        n1 = 2;
        n2 = 4;
        n3 = 6;
        n4 = 6.94f;
        l1 = "Bababooey";

        System.out.println(n1 + n2 + n3);   //Somas de INT
        System.out.println(n3 + n4);    //Soma de INT com Float

    }
}
